const cleanVerse = (text: string): string => {
    // This mapping fixes specific encoding errors where Latin characters replaced Arabic ones
    const map: Record<string, string> = {
        'a': 'ا', 'b': 'ب', 't': 'ت', 'c': 'ع', 'd': 'د', 
        'f': 'ف', 'g': 'غ', 'h': 'ه', 'i': 'ي', 'j': 'ج', 
        'k': 'ك', 'l': 'ل', 'm': 'م', 'n': 'ن', 'q': 'ق', 
        'r': 'ر', 's': 'س', 'u': 'و', 'v': 'ظ', 'w': 'و', 
        'x': 'خ', 'y': 'ي', 'z': 'ز',
        'L': 'ل', 'T': 'ط'
    };
    return text.replace(/[a-z]/g, m => map[m] || m);
};

